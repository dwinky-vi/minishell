#include "head_minishell.h"

int	is_special_character(char ch)
{
	if (ch == ' ' || ch == ';' || ch == '\\' || ch == '\'' || \
		ch == '\"' || ch == '|' || ch == '>' || ch == '<' || ch == '$')
		return (TRUE);
	return (FALSE);
}
